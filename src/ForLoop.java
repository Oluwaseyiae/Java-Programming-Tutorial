public class ForLoop {
    public static void main(String[] args) {

        System.out.println("Forward Order");
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        System.out.println("Reversed Order");
        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }
    }
}
